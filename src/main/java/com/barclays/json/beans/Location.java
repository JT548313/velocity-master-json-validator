package com.barclays.json.beans;

import java.net.URL;

public class Location {
	
	URL cloneUrl;
	String branch;
	
	public URL getCloneUrl() {
		return cloneUrl;
	}
	public void setCloneUrl(URL cloneUrl) {
		this.cloneUrl = cloneUrl;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	
}
